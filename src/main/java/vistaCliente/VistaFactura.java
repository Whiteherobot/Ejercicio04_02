    private void txtcalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtcalcularActionPerformed
       try{
        double unidades;
        double precio;
        double totalapagar;
        double cant_iva;
        double cant_des;
        double total_sin_iva;
        
        unidades=Double.parseDouble(txtunidades.getText());
        precio=Double.parseDouble(txtprecio.getText());
        total_sin_iva=precio*unidades;
        cant_iva=total_sin_iva*iva/100;
        cant_des=total_sin_iva*descuento/100;
        totalapagar=total_sin_iva+cant_iva-cant_des;
        txttotal.setText(" "+totalapagar);
       }catch(Exception ex){
           JOptionPane.showMessageDialog(this,"Error val - 15", "ERROR", JOptionPane.ERROR_MESSAGE);
       }
    }//GEN-LAST:event_txtcalcularActionPerformed

